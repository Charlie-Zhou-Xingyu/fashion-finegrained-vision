def load_json(path: Path) -> Dict[str, Any]:
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def save_json(data: Dict[str, Any], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)

    with path.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


def normalize_bbox_xyxy(raw_bbox: Any) -> Optional[List[float]]:
    """
    Normalize bbox to xyxy.

    Supports:
        [x1, y1, x2, y2]

    Args:
        raw_bbox: Raw bbox.

    Returns:
        bbox xyxy or None.
    """
    if not isinstance(raw_bbox, list):
        return None

    if len(raw_bbox) != 4:
        return None

    try:
        x1, y1, x2, y2 = [float(v) for v in raw_bbox]
    except Exception:
        return None

    if x2 <= x1 or y2 <= y1:
        return None

    return [x1, y1, x2, y2]


def get_image_path_from_prediction(prediction: Dict[str, Any]) -> Optional[str]:
    """
    Get image path from prediction JSON.
    """
    candidate_keys = [
        "image_path",
        "input_image_path",
        "source_image_path",
        "file_path",
    ]

    for key in candidate_keys:
        value = prediction.get(key)
        if isinstance(value, str) and value:
            return value

    image_info = prediction.get("image", {})
    if isinstance(image_info, dict):
        for key in candidate_keys:
            value = image_info.get(key)
            if isinstance(value, str) and value:
                return value

    return None


def get_instance_bbox(instance: Dict[str, Any]) -> Optional[List[float]]:
    """
    Get instance bbox.
    """
    candidate_keys = [
        "bbox_xyxy",
        "bbox",
        "box",
        "bounding_box",
    ]

    for key in candidate_keys:
        if key in instance:
            bbox = normalize_bbox_xyxy(instance[key])
            if bbox is not None:
                return bbox

    return None


def get_instance_category_name(instance: Dict[str, Any]) -> str:
    """
    Get category/class name from instance.
    """
    value = instance.get("class_name", instance.get("category_name", ""))
    if value is None:
        return ""
    return str(value)


def load_landmark_model(
    checkpoint_path: str,
    model_name: str,
    max_landmarks: int,
    device: torch.device,
) -> torch.nn.Module:
    """
    Load landmark predictor checkpoint.
    """
    model = build_landmark_model(
        model_name=model_name,
        max_landmarks=max_landmarks,
        pretrained=False,
    )

    checkpoint = torch.load(checkpoint_path, map_location=device)
    model.load_state_dict(checkpoint["model_state_dict"], strict=True)
    model.to(device)
    model.eval()

    return model


def crop_and_preprocess(
    image_rgb: np.ndarray,
    bbox_xyxy: List[float],
    image_size: int,
    pad_ratio: float,
) -> Tuple[torch.Tensor, Tuple[int, int, int, int]]:
    """
    Crop instance and convert to model input tensor.
    """
    image_h, image_w = image_rgb.shape[:2]

    crop_bbox = clip_bbox_to_image(
        bbox_xyxy=bbox_xyxy,
        image_width=image_w,
        image_height=image_h,
        pad_ratio=pad_ratio,
    )

    x1, y1, x2, y2 = crop_bbox
    crop = image_rgb[y1:y2, x1:x2]

    resized = cv2.resize(
        crop,
        (image_size, image_size),
        interpolation=cv2.INTER_LINEAR,
    )

    image_tensor = torch.from_numpy(resized).permute(2, 0, 1).float() / 255.0
    image_tensor = image_tensor.unsqueeze(0)

    return image_tensor, crop_bbox


def convert_pred_to_image_landmarks(
    pred_norm: np.ndarray,
    crop_bbox_xyxy: Tuple[int, int, int, int],
    max_landmarks: int,
) -> List[Dict[str, Any]]:
    """
    Convert normalized crop landmark predictions to original image coordinates.
    """
    x1, y1, x2, y2 = crop_bbox_xyxy
    crop_w = float(max(1, x2 - x1))
    crop_h = float(max(1, y2 - y1))

    landmarks: List[Dict[str, Any]] = []

    for i in range(max_landmarks):
        x_norm = float(pred_norm[i, 0])
        y_norm = float(pred_norm[i, 1])

        x_img = x_norm * crop_w + float(x1)
        y_img = y_norm * crop_h + float(y1)

        landmarks.append(
            {
                "index": i + 1,
                "x": float(x_img),
                "y": float(y_img),
                # Current baseline does not predict visibility.
                # We set 2 so landmark_refiner can use it.
                "visibility": 2,
                "source": "predicted",
                "score": None,
            }
        )

    return landmarks


def get_landmark_draw_color(landmark: Dict[str, Any]) -> Tuple[int, int, int]:
    """
    Get BGR color for landmark visualization according to quality/schema.

    Colors:
        valid ok: green
        refined_by_mask: cyan
        outside_mask: orange
        outside_mask_far: red
        invalid_for_class: gray
        no_mask: magenta
        empty_mask: purple
        invalid_point: gray
        default: red
    """
    valid_for_class = landmark.get("valid_for_class", True)
    if valid_for_class is False:
        return (128, 128, 128)

    quality = str(landmark.get("quality", ""))

    if quality == "ok":
        return (0, 255, 0)
    if quality == "refined_by_mask":
        return (255, 255, 0)
    if quality == "outside_mask":
        return (0, 165, 255)
    if quality == "outside_mask_far":
        return (0, 0, 255)
    if quality == "invalid_for_class":
        return (128, 128, 128)
    if quality == "no_mask":
        return (255, 0, 255)
    if quality == "empty_mask":
        return (128, 0, 128)
    if quality == "invalid_point":
        return (128, 128, 128)

    return (0, 0, 255)


def draw_predicted_landmarks(
    image_rgb: np.ndarray,
    instances: List[Dict[str, Any]],
    draw_index: bool = False,
    draw_name: bool = False,
) -> np.ndarray:
    """
    Draw predicted landmarks and bboxes on an RGB image.
    """
    image_bgr = cv2.cvtColor(image_rgb, cv2.COLOR_RGB2BGR)
    height, width = image_bgr.shape[:2]

    for instance in instances:
        bbox = get_instance_bbox(instance)
        if bbox is None:
            continue

        x1, y1, x2, y2 = [int(round(v)) for v in bbox]
        x1 = max(0, min(width - 1, x1))
        y1 = max(0, min(height - 1, y1))
        x2 = max(0, min(width - 1, x2))
        y2 = max(0, min(height - 1, y2))

        class_name = str(instance.get("class_name", instance.get("category_name", "")))
        confidence = instance.get("confidence", None)

        if confidence is not None:
            label = f"{class_name} {float(confidence):.2f}"
        else:
            label = class_name

        cv2.rectangle(image_bgr, (x1, y1), (x2, y2), (0, 255, 255), 2)
        cv2.putText(
            image_bgr,
            label,
            (x1, max(12, y1 - 6)),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.45,
            (0, 255, 255),
            1,
            cv2.LINE_AA,
        )

        landmarks = instance.get("landmarks", [])
        if not isinstance(landmarks, list):
            continue

        for landmark in landmarks:
            try:
                px = int(round(float(landmark["x"])))
                py = int(round(float(landmark["y"])))
            except Exception:
                continue

            px = max(0, min(width - 1, px))
            py = max(0, min(height - 1, py))

            color = get_landmark_draw_color(landmark)
            cv2.circle(image_bgr, (px, py), 3, color, -1)

            if bool(landmark.get("refined_by_mask", False)):
                cv2.circle(image_bgr, (px, py), 6, color, 1)

            if draw_index or draw_name:
                text = ""

                if draw_name:
                    name = landmark.get("name")
                    if isinstance(name, str) and name:
                        text = name
                    else:
                        text = str(int(landmark.get("index", 0)))
                else:
                    text = str(int(landmark.get("index", 0)))

                cv2.putText(
                    image_bgr,
                    text,
                    (px + 4, py - 4),
                    cv2.FONT_HERSHEY_SIMPLEX,
                    0.35,
                    color,
                    1,
                    cv2.LINE_AA,
                )

    return image_bgr


def build_instances_from_segments(
    segments: List[Dict[str, Any]],
) -> List[Dict[str, Any]]:
    """
    Convert SAM-HQ segment records to landmark inference instances.
    """
    instances: List[Dict[str, Any]] = []

    for segment in segments:
        if not isinstance(segment, dict):
            continue

        bbox = normalize_bbox_xyxy(segment.get("bbox_xyxy"))
        if bbox is None:
            continue

        instance = {
            "det_id": segment.get("det_id"),
            "class_id": segment.get("class_id"),
            "class_name": segment.get("class_name"),
            "confidence": segment.get("confidence"),
            "bbox_xyxy": bbox,
            "mask_path": segment.get("mask_path"),
            "mask_area": segment.get("mask_area"),
            "sam_score": segment.get("sam_score"),
        }
        instances.append(instance)

    return instances


def apply_landmark_schema(
    landmarks: List[Dict[str, Any]],
    instance: Dict[str, Any],
    add_landmark_schema: bool,
    filter_landmarks_by_class: bool,
    drop_invalid_landmarks: bool,
) -> List[Dict[str, Any]]:
    """
    Apply category schema if available and requested.
    """
    if not add_landmark_schema and not filter_landmarks_by_class:
        return landmarks

    if not HAS_LANDMARK_SCHEMA:
        print(
            "[WARN] --add-landmark-schema or --filter-landmarks-by-category was "
            "requested, but fashion_vision.landmarks.schema could not be imported. "
            "Skipping schema processing."
        )
        return landmarks

    category_name = get_instance_category_name(instance)

    if filter_landmarks_by_class:
        return filter_landmarks_by_category(
            landmarks=landmarks,
            category_name=category_name,
            drop_invalid=bool(drop_invalid_landmarks),
        )

    return enrich_landmarks_with_schema(
        landmarks=landmarks,
        category_name=category_name,
        mark_invalid_quality=False,
    )


def postprocess_landmarks(
    landmarks: List[Dict[str, Any]],
    instance: Dict[str, Any],
    use_mask_quality: bool,
    refine_with_mask: bool,
    max_mask_refine_distance: float,
    add_landmark_schema: bool,
    filter_landmarks_by_class: bool,
    drop_invalid_landmarks: bool,
) -> List[Dict[str, Any]]:
    """
    Apply optional schema and mask-based landmark post-processing.

    Order:
        1. Schema enrichment/filtering.
        2. Mask quality annotation.
        3. Optional mask refinement.
    """
    processed_landmarks = apply_landmark_schema(
        landmarks=landmarks,
        instance=instance,
        add_landmark_schema=add_landmark_schema,
        filter_landmarks_by_class=filter_landmarks_by_class,
        drop_invalid_landmarks=drop_invalid_landmarks,
    )

    mask_path = instance.get("mask_path")

    if use_mask_quality:
        processed_landmarks = annotate_landmarks_with_mask_quality(
            landmarks=processed_landmarks,
            mask_path=mask_path,
        )

    if refine_with_mask:
        processed_landmarks = refine_landmarks_to_mask(
            landmarks=processed_landmarks,
            mask_path=mask_path,
            max_distance_px=float(max_mask_refine_distance),
        )

    return processed_landmarks


def infer_one_instance(
    model: torch.nn.Module,
    image_rgb: np.ndarray,
    instance: Dict[str, Any],
    image_size: int,
    pad_ratio: float,
    max_landmarks: int,
    device: torch.device,
    use_mask_quality: bool = False,
    refine_with_mask: bool = False,
    max_mask_refine_distance: float = 20.0,
    add_landmark_schema: bool = False,
    filter_landmarks_by_class: bool = False,
    drop_invalid_landmarks: bool = False,
) -> Optional[Dict[str, Any]]:
    """
    Infer landmarks for one instance.
    """
    bbox = get_instance_bbox(instance)
    if bbox is None:
        return None

    image_tensor, crop_bbox = crop_and_preprocess(
        image_rgb=image_rgb,
        bbox_xyxy=bbox,
        image_size=image_size,
        pad_ratio=pad_ratio,
    )

    image_tensor = image_tensor.to(device)

    with torch.no_grad():
        pred = model(image_tensor)[0].detach().cpu().numpy()

    pred_landmarks = convert_pred_to_image_landmarks(
        pred_norm=pred,
        crop_bbox_xyxy=crop_bbox,
        max_landmarks=max_landmarks,
    )

    pred_landmarks = postprocess_landmarks(
        landmarks=pred_landmarks,
        instance=instance,
        use_mask_quality=use_mask_quality,
        refine_with_mask=refine_with_mask,
        max_mask_refine_distance=max_mask_refine_distance,
        add_landmark_schema=add_landmark_schema,
        filter_landmarks_by_class=filter_landmarks_by_class,
        drop_invalid_landmarks=drop_invalid_landmarks,
    )

    return {
        "landmarks": pred_landmarks,
        "crop_bbox_xyxy": list(crop_bbox),
    }


