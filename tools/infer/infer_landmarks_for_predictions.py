"""
Infer garment landmarks for existing SAM-HQ prediction JSON files.

This script loads a trained landmark predictor and writes predicted landmarks
back to prediction JSON files.

Input modes:
    1. Legacy prediction-json mode:
        --prediction-dir outputs/sam_hq_deepfashion2/predictions_with_landmarks

    2. Pipeline segmentation-json mode:
        --segmentation-json outputs/pipeline_13cls_full/02_samhq/segmentation_results.json

Output:
    Landmark JSON files or a pipeline landmarks_results.json.

Example, pipeline mode:
    python tools/infer/infer_landmarks_for_predictions.py ^
      --segmentation-json outputs/pipeline_13cls_full/02_samhq/segmentation_results.json ^
      --output-dir outputs/pipeline_13cls_full/03_landmarks_schema_quality ^
      --checkpoint outputs/landmark_predictor_resnet18/best.pt ^
      --model resnet18 ^
      --image-size 256 ^
      --max-landmarks 39 ^
      --pad-ratio 0.05 ^
      --device cuda ^
      --save-visualizations ^
      --draw-index ^
      --add-landmark-schema ^
      --use-mask-quality

Example, with mask refinement:
    python tools/infer/infer_landmarks_for_predictions.py ^
      --segmentation-json outputs/pipeline_13cls_full/02_samhq/segmentation_results.json ^
      --output-dir outputs/pipeline_13cls_full/03_landmarks_schema_refined ^
      --checkpoint outputs/landmark_predictor_resnet18/best.pt ^
      --model resnet18 ^
      --image-size 256 ^
      --max-landmarks 39 ^
      --pad-ratio 0.05 ^
      --device cuda ^
      --save-visualizations ^
      --draw-index ^
      --add-landmark-schema ^
      --use-mask-quality ^
      --refine-with-mask ^
      --max-mask-refine-distance 20
"""

