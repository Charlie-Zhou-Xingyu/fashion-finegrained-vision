def run_segmentation_json_mode(
    args: argparse.Namespace,
    model: torch.nn.Module,
    device: torch.device,
) -> None:
    """
    Run landmark inference from a SAM-HQ segmentation_results.json file.
    """
    segmentation_json = Path(args.segmentation_json)
    output_dir = Path(args.output_dir)

    if not segmentation_json.exists():
        raise FileNotFoundError(f"Segmentation JSON not found: {segmentation_json}")

    output_dir.mkdir(parents=True, exist_ok=True)

    vis_dir = output_dir / "visualizations"
    if args.save_visualizations:
        vis_dir.mkdir(parents=True, exist_ok=True)

    segmentation_data = load_json(segmentation_json)
    image_records = segmentation_data.get("images", [])

    if not isinstance(image_records, list):
        raise RuntimeError("Invalid segmentation JSON: 'images' must be a list.")

    run_config = build_run_config(args=args, device=device)

    results: Dict[str, Any] = {
        "task": "infer_landmarks_from_segmentation_results",
        "segmentation_json": str(segmentation_json),
        **run_config,
        "images": [],
    }

    num_images_processed = 0
    num_images_failed = 0
    num_instances_processed = 0
    num_instances_failed = 0

    for index, image_record in enumerate(image_records, start=1):
        image_path = image_record.get("image_path")
        print(f"[{index:04d}/{len(image_records):04d}] {image_path}")

        if not isinstance(image_path, str) or not image_path:
            print("[WARN] Missing image_path in segmentation image record.")
            num_images_failed += 1
            continue

        try:
            image_rgb = load_rgb_image(image_path)
        except Exception as exc:
            print(f"[WARN] Failed to read image {image_path}: {exc}")
            num_images_failed += 1
            continue

        segments = image_record.get("segments", [])
        if not isinstance(segments, list):
            print(f"[WARN] Invalid segments for image: {image_path}")
            num_images_failed += 1
            continue

        instances = build_instances_from_segments(segments)

        for instance in instances:
            result = infer_one_instance(
                model=model,
                image_rgb=image_rgb,
                instance=instance,
                image_size=int(args.image_size),
                pad_ratio=float(args.pad_ratio),
                max_landmarks=int(args.max_landmarks),
                device=device,
                use_mask_quality=bool(args.use_mask_quality),
                refine_with_mask=bool(args.refine_with_mask),
                max_mask_refine_distance=float(args.max_mask_refine_distance),
                add_landmark_schema=bool(args.add_landmark_schema),
                filter_landmarks_by_class=bool(args.filter_landmarks_by_category),
                drop_invalid_landmarks=bool(args.drop_invalid_landmarks),
            )

            if result is None:
                num_instances_failed += 1
                continue

            instance["landmarks"] = result["landmarks"]
            instance["landmark_source"] = f"{args.model}_landmark_predictor"
            instance["landmark_checkpoint"] = str(args.checkpoint)
            instance["landmark_crop_bbox_xyxy"] = result["crop_bbox_xyxy"]
            instance["landmark_image_size"] = int(args.image_size)

            if args.add_landmark_schema or args.filter_landmarks_by_category:
                instance["landmark_schema_enabled"] = True
                instance["landmark_schema_available"] = bool(HAS_LANDMARK_SCHEMA)

            if args.use_mask_quality:
                instance["landmark_mask_quality_enabled"] = True

            if args.refine_with_mask:
                instance["landmark_mask_refine_enabled"] = True
                instance["landmark_max_mask_refine_distance"] = float(
                    args.max_mask_refine_distance
                )

            num_instances_processed += 1

        visualization_path = None
        if args.save_visualizations:
            vis = draw_predicted_landmarks(
                image_rgb=image_rgb,
                instances=instances,
                draw_index=bool(args.draw_index),
                draw_name=bool(args.draw_name),
            )
            image_stem = Path(image_path).stem
            vis_path = vis_dir / f"{image_stem}_landmarks.jpg"
            cv2.imwrite(str(vis_path), vis)
            visualization_path = str(vis_path)

        result_record = {
            "image_id": image_record.get("image_id"),
            "image_path": image_path,
            "file_name": image_record.get("file_name", Path(image_path).name),
            "width": image_record.get("width"),
            "height": image_record.get("height"),
            "num_segments": len(segments),
            "num_landmark_instances": len(instances),
            "visualization_path": visualization_path,
            "instances": instances,
        }

        results["images"].append(result_record)
        num_images_processed += 1

    summary = {
        "num_images": len(image_records),
        "num_images_processed": num_images_processed,
        "num_images_failed": num_images_failed,
        "num_instances_processed": num_instances_processed,
        "num_instances_failed": num_instances_failed,
        **run_config,
    }

    results["summary"] = summary

    save_json(results, output_dir / "landmarks_results.json")
    save_json(summary, output_dir / "summary.json")

    print("[INFO] Landmark inference from segmentation JSON finished.")
    print(json.dumps(summary, ensure_ascii=False, indent=2))
    print(f"[INFO] Results: {output_dir / 'landmarks_results.json'}")

    if args.save_visualizations:
        print(f"[INFO] Visualizations: {vis_dir}")


def run_legacy_prediction_dir_mode(
    args: argparse.Namespace,
    model: torch.nn.Module,
    device: torch.device,
) -> None:
    """
    Run landmark inference for legacy per-image prediction JSON files.
    """
    prediction_dir = Path(args.prediction_dir)
    output_dir = Path(args.output_dir)

    if not prediction_dir.exists():
        raise FileNotFoundError(f"Prediction directory not found: {prediction_dir}")

    output_dir.mkdir(parents=True, exist_ok=True)

    prediction_paths = sorted(prediction_dir.glob("*.json"))

    if args.max_files > 0:
        prediction_paths = prediction_paths[: args.max_files]

    print(f"[INFO] Prediction files: {len(prediction_paths)}")

    num_files_processed = 0
    num_files_failed = 0
    num_instances_processed = 0
    num_instances_failed = 0

    for pred_path in prediction_paths:
        try:
            prediction = load_json(pred_path)
        except Exception as exc:
            print(f"[WARN] Failed to load {pred_path}: {exc}")
            num_files_failed += 1
            continue

        image_path = get_image_path_from_prediction(prediction)

        if image_path is None:
            print(f"[WARN] Missing image path in {pred_path}")
            num_files_failed += 1
            continue

        try:
            image_rgb = load_rgb_image(image_path)
        except Exception as exc:
            print(f"[WARN] Failed to read image {image_path}: {exc}")
            num_files_failed += 1
            continue

        instances = prediction.get("instances", [])
        if not isinstance(instances, list):
            print(f"[WARN] Invalid instances in {pred_path}")
            num_files_failed += 1
            continue

        for instance in instances:
            if not isinstance(instance, dict):
                continue

            if instance.get("landmarks") and not args.overwrite_landmarks:
                continue

            result = infer_one_instance(
                model=model,
                image_rgb=image_rgb,
                instance=instance,
                image_size=args.image_size,
                pad_ratio=args.pad_ratio,
                max_landmarks=args.max_landmarks,
                device=device,
                use_mask_quality=bool(args.use_mask_quality),
                refine_with_mask=bool(args.refine_with_mask),
                max_mask_refine_distance=float(args.max_mask_refine_distance),
                add_landmark_schema=bool(args.add_landmark_schema),
                filter_landmarks_by_class=bool(args.filter_landmarks_by_category),
                drop_invalid_landmarks=bool(args.drop_invalid_landmarks),
            )

            if result is None:
                num_instances_failed += 1
                continue

            instance["landmarks"] = result["landmarks"]
            instance["landmark_source"] = f"{args.model}_landmark_predictor"
            instance["landmark_checkpoint"] = str(args.checkpoint)
            instance["landmark_crop_bbox_xyxy"] = result["crop_bbox_xyxy"]
            instance["landmark_image_size"] = int(args.image_size)

            if args.add_landmark_schema or args.filter_landmarks_by_category:
                instance["landmark_schema_enabled"] = True
                instance["landmark_schema_available"] = bool(HAS_LANDMARK_SCHEMA)

            if args.use_mask_quality:
                instance["landmark_mask_quality_enabled"] = True

            if args.refine_with_mask:
                instance["landmark_mask_refine_enabled"] = True
                instance["landmark_max_mask_refine_distance"] = float(
                    args.max_mask_refine_distance
                )

            num_instances_processed += 1

        output_path = output_dir / pred_path.name
        save_json(prediction, output_path)
        num_files_processed += 1

        if num_files_processed % 50 == 0:
            print(
                f"[INFO] Processed files: {num_files_processed}/{len(prediction_paths)} "
                f"instances: {num_instances_processed}"
            )

    run_config = build_run_config(args=args, device=device)

    summary = {
        "prediction_dir": str(prediction_dir),
        "output_dir": str(output_dir),
        "num_files": len(prediction_paths),
        "num_files_processed": num_files_processed,
        "num_files_failed": num_files_failed,
        "num_instances_processed": num_instances_processed,
        "num_instances_failed": num_instances_failed,
        "landmark_source": f"{args.model}_landmark_predictor",
        **run_config,
    }

    save_json(summary, output_dir / "summary.json")

    print("[INFO] Done.")
    print(json.dumps(summary, ensure_ascii=False, indent=2))


def main() -> None:
    """
    Command line entry point.

    Supports two modes:
        1. Legacy mode: --prediction-dir
        2. Pipeline mode: --segmentation-json
    """
    args = parse_args()

    if not args.prediction_dir and not args.segmentation_json:
        raise ValueError(
            "Either --prediction-dir or --segmentation-json must be provided."
        )

    if args.prediction_dir and args.segmentation_json:
        raise ValueError(
            "Please provide only one input mode: "
            "--prediction-dir or --segmentation-json."
        )

    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    if args.device == "cuda" and not torch.cuda.is_available():
        print("[WARN] CUDA requested but not available. Falling back to CPU.")
        device = torch.device("cpu")
    else:
        device = torch.device(args.device)

    print(f"[INFO] Device: {device}")

    if (args.add_landmark_schema or args.filter_landmarks_by_category) and not HAS_LANDMARK_SCHEMA:
        print(
            "[WARN] Landmark schema module is not available. "
            "Please create src/fashion_vision/landmarks/schema.py if you want "
            "valid_for_class/name/region fields."
        )

    model = load_landmark_model(
        checkpoint_path=args.checkpoint,
        model_name=args.model,
        max_landmarks=args.max_landmarks,
        device=device,
    )

    if args.segmentation_json:
        run_segmentation_json_mode(
            args=args,
            model=model,
            device=device,
        )
        return

    run_legacy_prediction_dir_mode(
        args=args,
        model=model,
        device=device,
    )


if __name__ == "__main__":
    main()
