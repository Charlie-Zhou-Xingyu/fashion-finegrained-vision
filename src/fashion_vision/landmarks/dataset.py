"""
DeepFashion2 garment landmark dataset.

This dataset reads JSONL files generated by:
    tools/build_deepfashion2_landmark_dataset.py

It crops garment instances by bbox, resizes them to a fixed input size,
and converts original image landmark coordinates into normalized crop
coordinates.

Output sample:
    {
        "image": FloatTensor [3, H, W], range [0, 1],
        "landmarks": FloatTensor [max_landmarks, 2], normalized to [0, 1],
        "visibility": LongTensor [max_landmarks], 0 absent, 1 occluded, 2 visible,
        "valid": FloatTensor [max_landmarks], 1 if visibility > 0 else 0,
        "category_id": int,
        "category_name": str,
        "image_id": str,
        "instance_id": str,
        "image_path": str,
        "bbox_xyxy": list[float],
    }
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

import cv2
import numpy as np
import torch
from torch.utils.data import Dataset


DEEFPASHION2_MAX_LANDMARKS = 39


def read_jsonl(path: str | Path) -> List[Dict[str, Any]]:
    """
    Read JSONL file.

    Args:
        path: JSONL path.

    Returns:
        Records.
    """
    records: List[Dict[str, Any]] = []
    path = Path(path)

    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            records.append(json.loads(line))

    return records


def load_rgb_image(image_path: str | Path) -> np.ndarray:
    """
    Load RGB image.

    Args:
        image_path: Image path.

    Returns:
        RGB image, uint8, HWC.
    """
    image_path = str(image_path)
    bgr = cv2.imread(image_path, cv2.IMREAD_COLOR)

    if bgr is None:
        raise FileNotFoundError(f"Failed to read image: {image_path}")

    rgb = cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)
    return rgb


def clip_bbox_to_image(
    bbox_xyxy: List[float],
    image_width: int,
    image_height: int,
    pad_ratio: float = 0.05,
) -> Tuple[int, int, int, int]:
    """
    Clip bbox to image boundary and optionally pad.

    Args:
        bbox_xyxy: bbox in original image coordinates.
        image_width: Image width.
        image_height: Image height.
        pad_ratio: Padding ratio relative to bbox size.

    Returns:
        Clipped int bbox x1, y1, x2, y2.
    """
    x1, y1, x2, y2 = [float(v) for v in bbox_xyxy]

    w = max(1.0, x2 - x1)
    h = max(1.0, y2 - y1)

    pad_x = w * pad_ratio
    pad_y = h * pad_ratio

    x1 -= pad_x
    y1 -= pad_y
    x2 += pad_x
    y2 += pad_y

    x1_i = int(max(0, min(image_width - 1, round(x1))))
    y1_i = int(max(0, min(image_height - 1, round(y1))))
    x2_i = int(max(1, min(image_width, round(x2))))
    y2_i = int(max(1, min(image_height, round(y2))))

    if x2_i <= x1_i:
        x2_i = min(image_width, x1_i + 1)

    if y2_i <= y1_i:
        y2_i = min(image_height, y1_i + 1)

    return x1_i, y1_i, x2_i, y2_i


def convert_landmarks_to_crop_normalized(
    raw_landmarks: List[Dict[str, Any]],
    crop_bbox_xyxy: Tuple[int, int, int, int],
    max_landmarks: int = DEEFPASHION2_MAX_LANDMARKS,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Convert original image landmarks to crop-normalized coordinates.

    Args:
        raw_landmarks: Landmark list in original image coordinates.
        crop_bbox_xyxy: Crop bbox x1, y1, x2, y2 in original image.
        max_landmarks: Maximum landmark count.

    Returns:
        landmarks: [max_landmarks, 2], normalized coordinates.
        visibility: [max_landmarks], 0/1/2.
        valid: [max_landmarks], 1 if visibility > 0 and point inside crop.
    """
    x1, y1, x2, y2 = crop_bbox_xyxy
    crop_w = max(1.0, float(x2 - x1))
    crop_h = max(1.0, float(y2 - y1))

    landmarks = np.zeros((max_landmarks, 2), dtype=np.float32)
    visibility = np.zeros((max_landmarks,), dtype=np.int64)
    valid = np.zeros((max_landmarks,), dtype=np.float32)

    for lm in raw_landmarks:
        index = int(lm.get("index", 0))
        if index <= 0 or index > max_landmarks:
            continue

        lm_x = float(lm.get("x", 0.0))
        lm_y = float(lm.get("y", 0.0))
        lm_v = int(lm.get("visibility", 0))

        dst_index = index - 1
        visibility[dst_index] = lm_v

        if lm_v <= 0 or lm_x <= 0 or lm_y <= 0:
            continue

        x_norm = (lm_x - float(x1)) / crop_w
        y_norm = (lm_y - float(y1)) / crop_h

        landmarks[dst_index, 0] = np.float32(x_norm)
        landmarks[dst_index, 1] = np.float32(y_norm)

        # Keep only points inside or near crop.
        if -0.05 <= x_norm <= 1.05 and -0.05 <= y_norm <= 1.05:
            valid[dst_index] = 1.0

    return landmarks, visibility, valid


class DeepFashion2LandmarkDataset(Dataset):
    """
    DeepFashion2 garment landmark dataset.
    """

    def __init__(
        self,
        jsonl_path: str | Path,
        image_size: int = 256,
        max_landmarks: int = DEEFPASHION2_MAX_LANDMARKS,
        pad_ratio: float = 0.05,
        categories: Optional[List[str]] = None,
    ) -> None:
        """
        Args:
            jsonl_path: Dataset JSONL path.
            image_size: Output square image size.
            max_landmarks: Max landmark count.
            pad_ratio: Bbox crop padding ratio.
            categories: Optional category name whitelist.
        """
        self.jsonl_path = Path(jsonl_path)
        self.image_size = int(image_size)
        self.max_landmarks = int(max_landmarks)
        self.pad_ratio = float(pad_ratio)

        records = read_jsonl(self.jsonl_path)

        if categories is not None:
            category_set = {c.lower().strip() for c in categories}
            records = [
                r for r in records
                if str(r.get("category_name", "")).lower().strip() in category_set
            ]

        self.records = records

    def __len__(self) -> int:
        return len(self.records)

    def __getitem__(self, index: int) -> Dict[str, Any]:
        record = self.records[index]

        image = load_rgb_image(record["image_path"])
        image_h, image_w = image.shape[:2]

        crop_bbox = clip_bbox_to_image(
            bbox_xyxy=record["bbox_xyxy"],
            image_width=image_w,
            image_height=image_h,
            pad_ratio=self.pad_ratio,
        )

        x1, y1, x2, y2 = crop_bbox
        crop = image[y1:y2, x1:x2]

        resized = cv2.resize(
            crop,
            (self.image_size, self.image_size),
            interpolation=cv2.INTER_LINEAR,
        )

        landmarks, visibility, valid = convert_landmarks_to_crop_normalized(
            raw_landmarks=record["landmarks"],
            crop_bbox_xyxy=crop_bbox,
            max_landmarks=self.max_landmarks,
        )

        # Clamp coordinates for training stability.
        landmarks = np.clip(landmarks, 0.0, 1.0)

        image_tensor = torch.from_numpy(resized).permute(2, 0, 1).float() / 255.0
        landmark_tensor = torch.from_numpy(landmarks).float()
        visibility_tensor = torch.from_numpy(visibility).long()
        valid_tensor = torch.from_numpy(valid).float()

        sample: Dict[str, Any] = {
            "image": image_tensor,
            "landmarks": landmark_tensor,
            "visibility": visibility_tensor,
            "valid": valid_tensor,
            "category_id": int(record["category_id"]),
            "category_name": str(record["category_name"]),
            "image_id": str(record["image_id"]),
            "instance_id": str(record["instance_id"]),
            "image_path": str(record["image_path"]),
            "bbox_xyxy": record["bbox_xyxy"],
            "crop_bbox_xyxy": list(crop_bbox),
            "num_landmarks": int(record["num_landmarks"]),
            "num_visible_landmarks": int(record["num_visible_landmarks"]),
            "num_present_landmarks": int(record["num_present_landmarks"]),
        }

        return sample
